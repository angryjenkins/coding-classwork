a=float(input("Choose a number. "))
operation=int(input("Enter an operation - 1 for addition, 2 for subtraction, 3 for multiplication, or 4 for division. "))
b=float(input("Choose a second number. "))

if operation is 1:
	print (a + b)
elif operation is 2:
	print (a - b)
elif operation is 3:
	print (a * b)
elif operation is 4:
	print (a / b)

